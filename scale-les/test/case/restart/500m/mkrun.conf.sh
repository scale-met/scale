#!/bin/bash

RUN_TYPES=( "0" "A" "B" )

for RUN_TYPE in ${RUN_TYPES[*]}
do
  CONFNAME="run.restart${RUN_TYPE}.conf"

  case ${RUN_TYPE} in
    "0" )
      TIME_STARTDATE="0000, 1, 1, 0, 0, 0"
      TIME_DURATION="8.D0"
      RESTART_RUN=".false."
      RESTART_IN_BASENAME="init_00000101-000000.000"
    ;;
