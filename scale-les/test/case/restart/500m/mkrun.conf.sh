/

&PARAM_GRID
 DZ = 500.D0,
 DX = 500.D0,
 DY = 500.D0,
/

&PARAM_LAND_GRID
 LDZ = 0.05D0, 0.15D0, 0.30D0, 0.50D0, 1.00D0,
/

&PARAM_URBAN_GRID
 UDZ = 0.01D0, 0.01D0, 0.03D0, 0.05D0, 0.10D0,
/

&PARAM_TIME
 TIME_STARTDATE             = ${TIME_STARTDATE},
 TIME_STARTMS               = 0.D0,
 TIME_DURATION              = ${TIME_DURATION},
 TIME_DURATION_UNIT         = "SEC",
 TIME_DT                    = 1.D0,
 TIME_DT_UNIT               = "SEC",
 TIME_DT_ATMOS_DYN          = 0.2D0,
 TIME_DT_ATMOS_DYN_UNIT     = "SEC",
 TIME_DT_ATMOS_PHY_MP       = 2.D0,
 TIME_DT_ATMOS_PHY_MP_UNIT  = "SEC",
 TIME_DT_ATMOS_PHY_RD       = 4.D0,
 TIME_DT_ATMOS_PHY_RD_UNIT  = "SEC",
 TIME_DT_ATMOS_PHY_TB       = 2.D0,
 TIME_DT_ATMOS_PHY_TB_UNIT  = "SEC",
 TIME_DT_OCEAN              = 4.D0,
 TIME_DT_OCEAN_UNIT         = "SEC",
 TIME_DT_LAND               = 4.D0,
 TIME_DT_LAND_UNIT          = "SEC",
 TIME_DT_URBAN              = 4.D0,
 TIME_DT_URBAN_UNIT         = "SEC",
/

&PARAM_RESTART
 RESTART_RUN          = ${RESTART_RUN},
 RESTART_OUTPUT       = .true.,
 RESTART_IN_BASENAME  = "${RESTART_IN_BASENAME}",
 RESTART_OUT_BASENAME = "restart${RUN_TYPE}",
/

&PARAM_MAPPROJ
 MPRJ_basepoint_lon = 180.D0,
 MPRJ_basepoint_lat = 0.D0,
/

&PARAM_LANDUSE
 LANDUSE_MosaicWorld = .true.,
/

&PARAM_STATISTICS
