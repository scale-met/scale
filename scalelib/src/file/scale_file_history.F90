    character(len=FILE_HSHORT) :: FILE_HISTORY_OUTPUT_SWITCH_TUNIT       !> Time unit

    NAMELIST / PARAM_FILE_HISTORY / &
       FILE_HISTORY_TITLE,                     &
       FILE_HISTORY_SOURCE,                    &
       FILE_HISTORY_INSTITUTION,               &
